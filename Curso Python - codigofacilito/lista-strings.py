my_string = 'Curso de Código Facilito!'
print(my_string)
print(my_string[0]) # obtener caracter segun posición de izquierda a derecha
print(my_string[-1]) # obtener caracter segun posición de  derecha a  izquierda
print(my_string[0],my_string[-1])

print(my_string[0:10]) # obtenerdesde una posición hasta otra
print(my_string[-2:-1])

print(my_string[0:10:2]) # obtenerdesde una posición hasta otra con salto
print(my_string[::-1]) # leer de derecha a izquierda